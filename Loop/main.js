// For loop
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// For of loop 
let myArray = ["Luke", "Han", "Chewbacca", "Leia"];

for (const arrayItem of myArray) {
    console.log(arrayItem)
}

// While   
let i = 1;

while (i <= 100) {
    console.log(i);
    i++;
}

// do-while
let i1 = 1;

do {
    console.log(i1);
    i1++;
} while (i1 <= 100);

